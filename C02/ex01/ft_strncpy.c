char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	char			*str;
	unsigned int	i;

	str = dest;
	i = 0;
	while (*src && i < n)
	{
		*dest = *src;
		dest++;
		src++;
		i++;
	}
	while (i < n)
	{
		*dest = '\0';
		dest++;
		i++;
	}
	return (str);
}
